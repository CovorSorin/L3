package model;

public class Cours {

	private String jour;
	private String nom;
	private int heure;
	private int pointsCredit;

	public Cours(String jour, String nom, int heure, int pointsCredit) {
		super();
		this.jour = jour;
		this.nom = nom;
		this.heure = heure;
		this.pointsCredit = pointsCredit;
	}

}
